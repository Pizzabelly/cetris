void apply_test_board(struct cetris_game* g, enum tests t) {
  int (*board)[20][10];
  switch (t) {
    case TSPIN:
      board = &tspin_test_board;
      break;
    default:
      return;
  }
  for (int y = 0; y < 20; y++) {
    for (int x = 0; x < 10; x++) {
      if ((*board)[y][x]) {
        g->board[x][y].occupied = 1;
        g->board[x][y].constant = 1;
      }
    }
  }
}

